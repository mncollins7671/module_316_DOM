// 1. Log the entire document object to the console
console.log(document);

// 2. Log the page title to the console
console.log(document.title);

// 3. Log the current URL of the page to the console
console.log(document.URL);

// 4. Log the <body> element to the console
console.log(document.body);

// 5. Log the <head> element to the console
console.log(document.head);

// 6. Log the <html> element (documentElement) to the console
console.log(document.documentElement);

// 7. Log the character set of the document
console.log(document.characterSet);

// 8. Log the content type of the document
console.log(document.contentType);

// BONUS: Try changing the document title and observe what happens
